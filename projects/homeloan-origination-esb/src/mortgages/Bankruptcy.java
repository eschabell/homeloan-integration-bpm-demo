package mortgages;

public class Bankruptcy {
	private int amountOwed;
	private int yearOfOccurrence;
	/**
	 * @return the amountOwed
	 */
	public int getAmountOwed() {
		return amountOwed;
	}
	/**
	 * @param amountOwed the amountOwed to set
	 */
	public void setAmountOwed(int amountOwed) {
		this.amountOwed = amountOwed;
	}
	/**
	 * @return the yearOfOccurrence
	 */
	public int getYearOfOccurrence() {
		return yearOfOccurrence;
	}
	/**
	 * @param yearOfOccurrence the yearOfOccurrence to set
	 */
	public void setYearOfOccurrence(int yearOfOccurrence) {
		this.yearOfOccurrence = yearOfOccurrence;
	}

}
